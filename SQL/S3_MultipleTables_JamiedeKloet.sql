-- BOEK OPDRACHTEN:
-- 1a:
SELECT C.CODE, U.BEGINDATUM, C.LENGTE, M.NAAM AS Docent
FROM MEDEWERKERS M, CURSUSSEN C, UITVOERINGEN U
WHERE U.DOCENT = M.MNR AND U.CURSUS = C.CODE;

-- 1b:
SELECT DISTINCT C.CODE, U.BEGINDATUM, C.LENGTE, CASE WHEN U.DOCENT IS NOT NULL THEN M.NAAM ELSE NULL END AS Docent
FROM MEDEWERKERS M, CURSUSSEN C, UITVOERINGEN U WHERE COALESCE(U.DOCENT, -1) IN (M.MNR, -1)
AND U.CURSUS = C.CODE;

-- 2:
SELECT M.NAAM AS Deelnemer, D.NAAM AS Docent
FROM MEDEWERKERS D
    JOIN UITVOERINGEN U ON (U.DOCENT = D.MNR)
    JOIN INSCHRIJVINGEN I USING (CURSUS, BEGINDATUM)
    JOIN MEDEWERKERS M ON (I.CURSIST = M.MNR)
WHERE CURSUS = 'S02';


