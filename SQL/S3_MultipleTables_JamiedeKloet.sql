FROM INSCHRIJVINGEN I, MEDEWERKERS M
WHERE I.CURSUS='S02' AND I.BEGINDATUM LIKE TO_DATE('1999-04-12', 'yyyy-mm-dd') AND I.CURSIST = M.MNR;


