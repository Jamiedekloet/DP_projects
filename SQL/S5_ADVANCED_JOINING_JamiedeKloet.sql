SELECT CURSIST
FROM INSCHRIJVINGEN
WHERE CURSUS = 'JAV' AND CURSIST IN (
    SELECT CURSIST
    FROM INSCHRIJVINGEN
    WHERE CURSUS = 'XML'
    );


-- H4, 07:
SELECT M.MNR, A.NAAM
FROM MEDEWERKERS M, AFDELINGEN A
WHERE M.AFD = A.ANR AND (A.NAAM != 'OPLEIDINGEN');


-- H4, 08:
SELECT MNR
FROM MEDEWERKERS
WHERE MNR NOT IN (
    SELECT CURSIST
    FROM INSCHRIJVINGEN
    WHERE CURSUS = 'JAV'
    );

-- H4, 10:
SELECT MNR, NAAM, VOORL
FROM MEDEWERKERS
WHERE MNR IN (
    SELECT CHEF
    FROM MEDEWERKERS
    );

-- H4, 11:
SELECT *
FROM UITVOERINGEN U, CURSUSSEN C
WHERE U.CURSUS = C.CODE AND (C.TYPE = 'ALG' AND EXTRACT(YEAR FROM U.BEGINDATUM) = 1999);


