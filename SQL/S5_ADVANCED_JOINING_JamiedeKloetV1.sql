SELECT U.CURSUS, U.BEGINDATUM, COUNT(I.CURSUS) AS AANTAL_INSCHRIJVINGEN
FROM UITVOERINGEN U, INSCHRIJVINGEN I
WHERE U.CURSUS = I.CURSUS
GROUP BY U.BEGINDATUM, U.CURSUS;

-- H8, 09:
SELECT DISTINCT M.NAAM, M.VOORL
FROM MEDEWERKERS M, CURSUSSEN C, INSCHRIJVINGEN I
WHERE  M.FUNCTIE = 'TRAINER' AND (I.CURSIST = M.MNR AND C.TYPE = 'ALG');

